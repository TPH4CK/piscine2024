int	ft_atoi(char *str)
{
	int	i;
	int	sign;
	int	res;

	i = 0;
	sign = 1;
	res = -1;
	while (str[i] >= '0' && str[i] <= '9')
	{
		if (res == -1)
			res++;
		res *= 10;
		res += str[i] - 48;
		i++;
	}
	if (str[i] != '\0' && (str[i] < '0' || str[i] > '9'))
		return (-1);
	return (sign * res);
}
