#include <stdio.h>

void	ft_ultimate_div_mod(int *a, int *b)
{
	int	div;
	int	mod;

	if (*b != 0)
	{
		mod = *a % *b;
		div = *a / *b;
		*a = div;
		*b = mod;
	}
}
